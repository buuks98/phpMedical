-- per assignar codi de visita i ordre si no es donen
CREATE OR REPLACE TRIGGER generaCodiOrdreVisites
    BEFORE INSERT ON Visites
    FOR EACH ROW
BEGIN
    IF :NEW.codi IS NULL THEN
        SELECT MAX(codi)+1 INTO :NEW.codi FROM Visites;
    END IF;
    IF :NEW.ordre IS NULL THEN
        SELECT MAX(ordre)+1 INTO :NEW.ordre FROM Visites WHERE pacient=:NEW.pacient;
    END IF;
END;
/

